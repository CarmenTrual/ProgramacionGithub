/* Variables.java
* Declara las variables enteras x e y con los valores 144 y 999 respectivamente. 
Muestra por pantalla el valor de cada variable, la suma, la resta, la división 
y la multiplicación.
* @CarmenTrual
*/
	public class Variables {
		public static void main (String[] args) {
        int  x;
        x = 144;
        int  y;
        y = 999;
        System.out.println("El valor de x es:" + (x));
        System.out.println("Su suma es:" + (x+y));
        System.out.println("Su resta es:" + (x-y));
        System.out.println("Su division es:" + (x/y));
        System.out.println("Su multiplicacion es:" + (x*y));
    }
  }

