/* C5Ejercicio2.java
* Programa que muestra los números múltiplos de 5 de 0 a 100 utilizando 
un bucle while.
* @CarmenTrual
*/
public class C5Ejercicio2 {
  public static void main(String[] args) {
    
    int mul5 = 0; 
    
    while(mul5<=100) {
      System.out.println(mul5);
      mul5+=5;
    }
  }
}
